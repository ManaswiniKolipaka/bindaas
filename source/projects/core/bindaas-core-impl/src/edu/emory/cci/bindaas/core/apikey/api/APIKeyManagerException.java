package edu.emory.cci.bindaas.core.apikey.api;

public class APIKeyManagerException extends Exception{
	private static final long serialVersionUID = 1L;

	public APIKeyManagerException() {
		super();

	}

	public APIKeyManagerException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public APIKeyManagerException(String arg0) {
		super(arg0);

	}

	public APIKeyManagerException(Throwable arg0) {
		super(arg0);

	}

}

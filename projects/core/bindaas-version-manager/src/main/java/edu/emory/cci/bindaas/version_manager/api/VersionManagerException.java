package edu.emory.cci.bindaas.version_manager.api;

public class VersionManagerException extends Exception {
	private static final long serialVersionUID = 148083025094364737L;

	public VersionManagerException() {
		super();
	
	}

	public VersionManagerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	
	}

	public VersionManagerException(String arg0) {
		super(arg0);
	
	}

	public VersionManagerException(Throwable arg0) {
		super(arg0);
	
	}

	
}
